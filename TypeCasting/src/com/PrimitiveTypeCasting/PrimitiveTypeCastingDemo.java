package com.PrimitiveTypeCasting;

public class PrimitiveTypeCastingDemo {

	public static void main(String[] args) {
		//implicit Primitive Type Casting
		int i = 10;
		long j = i; 

		System.out.println("j : " +j);
		
		//Explicit Primitive Type Casting
        double d = 1000.00;
        float f = (float)d;
        System.out.println("d: " +d);
        
      }

}
